import { cleanup, render, screen } from "@testing-library/react";
import { afterEach, describe, expect, it, vi } from "vitest";

import { FilingDeadlinesPanel } from "@/components/period/FilingDeadlinesPanel";
import { FilingOutputsPanel } from "@/components/period/FilingOutputsPanel";
import { PeriodAdjustmentsWorkspace } from "@/components/period/PeriodAdjustmentsWorkspace";
import { YearEndMoneyListSection } from "@/components/period/YearEndMoneyListSection";
import { permissionsForRole, type PlatformRole } from "@/lib/permissions";

afterEach(cleanup);

describe.each(["Owner", "Accountant", "Reviewer", "Client"] satisfies PlatformRole[])("%s permission surfaces", (role) => {
  const permissions = permissionsForRole(role);

  it("matches working-paper and professional-review actions without dead controls", () => {
    const { unmount } = render(
      <PeriodAdjustmentsWorkspace
        canWrite={permissions.canWriteWorkingPapers}
        canApprove={permissions.canApprove}
        adjustments={[{
          id: 11,
          description: "Accrual",
          amount: 100,
          source: "Manual",
          impactOnProfit: -100,
          impactOnAssets: 0,
          isAuto: false,
        }]}
        adjSummary={{
          autoGenerated: 0,
          manual: 1,
          pendingApproval: 1,
          approved: 0,
          totalImpactOnProfit: -100,
          totalImpactOnAssets: 0,
        }}
        loadingAdjustments={false}
        generatingAdj={false}
        approvingId={null}
        adjFilterApproved=""
        adjFilterType=""
        onGenerateAdjustments={vi.fn()}
        onRefreshAdjustments={vi.fn()}
        onApproveAdjustment={vi.fn()}
        onFilterApprovedChange={vi.fn()}
        onFilterTypeChange={vi.fn()}
      />,
    );

    expect(Boolean(screen.queryByRole("button", { name: /generate adjustments/i }))).toBe(permissions.canWriteWorkingPapers);
    expect(Boolean(screen.queryByRole("button", { name: /approve/i }))).toBe(permissions.canApprove);
    unmount();

    render(
      <FilingOutputsPanel
        canRead={permissions.canRead}
        canGenerate={permissions.canWriteWorkingPapers}
        filingRegimeReady
        downloadingDocument={null}
        checklist={{
          transactionsCategorised: true,
          adjustmentsReviewed: true,
          balanceSheetBalances: true,
          filingReadinessComplete: true,
          accountsPdfGenerated: true,
          croPackAndSignatureGenerated: true,
        }}
        onDownloadAgmPack={vi.fn()}
        onDownloadCroFilingPack={vi.fn()}
        onDownloadSignaturePage={vi.fn()}
        onDownloadIxbrl={vi.fn()}
      />,
    );

    expect(Boolean(screen.queryByRole("button", { name: /download agm pdf/i }))).toBe(permissions.canRead);
    expect(Boolean(screen.queryByRole("button", { name: /download cro pdf/i }))).toBe(permissions.canWriteWorkingPapers);
    expect(Boolean(screen.queryByRole("button", { name: /download draft review xhtml/i }))).toBe(permissions.canRead);
    cleanup();

    render(
      <FilingDeadlinesPanel
        canReview={permissions.canReview}
        deadlines={[{
          id: 21,
          companyId: 7,
          periodId: 3,
          deadlineType: "Revenue",
          calculatedDueDate: "2026-09-23",
          dueDate: "2026-09-23",
          isLate: false,
          penaltyAmount: 0,
        }]}
        filingStatus={null}
        filingReferences={{}}
        markingFiledId={null}
        onFilingReferenceChange={vi.fn()}
        onMarkFiled={vi.fn()}
        onReferenceMissing={vi.fn()}
      />,
    );

    expect(Boolean(screen.queryByRole("button", { name: /mark as filed/i }))).toBe(permissions.canReview);
    expect(Boolean(screen.queryByLabelText(/revenue ros or ct1 filing reference/i))).toBe(permissions.canReview);
    cleanup();

    render(
      <YearEndMoneyListSection
        canWrite={permissions.canWriteWorkingPapers}
        mode="debtors"
        items={[{ id: 31, name: "Customer", amount: 50, type: "Trade" }]}
        draft={{ name: "", amount: 0, type: "Trade" }}
        typeOptions={["Trade", "Other"]}
        namePlaceholder="Who owes you?"
        saving={false}
        onDraftChange={vi.fn()}
        onAdd={vi.fn()}
        onDelete={vi.fn()}
      />,
    );

    expect(Boolean(screen.queryByRole("button", { name: /add debtor/i }))).toBe(permissions.canWriteWorkingPapers);
    expect(Boolean(screen.queryByRole("button", { name: /delete debtor customer/i }))).toBe(permissions.canWriteWorkingPapers);
  });
});
